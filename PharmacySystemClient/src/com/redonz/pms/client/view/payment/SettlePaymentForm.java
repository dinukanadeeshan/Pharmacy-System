    private void orderTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_orderTableMouseClicked
        try {
            int selectedRow = orderTable.getSelectedRow();
            String orderId = (String) orderModel.getValueAt(selectedRow, 1);
            if (currentReservedID != null) {
                ServerConnector.getServerConnector().getCustomerOrderController().releaseCustomerOrder(currentReservedID);
            }
            boolean isReserved = ServerConnector.getServerConnector().getCustomerOrderController().reserveCustomerOrder(orderId);
            if (isReserved) {
                currentReservedID = orderId;
                String custId = (String) orderModel.getValueAt(selectedRow, 0);
                for (int i = 0; i < customerComboBox.getItemCount(); i++) {
                    if (customerComboBox.getItemAt(i).toString().contains(custId)) {
                        customerComboBox.setSelectedIndex(i);
                        break;
                    }
                }
                paymentAmountTextField.setEditable(true);
                paymentAmountTextField.setFocusable(true);
                paymentHistoryButton.setEnabled(true);
                dueAmountTextField.setText(orderModel.getValueAt(selectedRow, 4).toString());
                paymentAmountTextField.setText(orderModel.getValueAt(selectedRow, 4).toString());
                try {
                    ArrayList<CustomerOrderDetail> orderDetails = ServerConnector.getServerConnector().getCustomerOderDetailController().getOrderDetailsByOrderId(orderId);
                    fillOrderDetailTable(orderDetails);
                } catch (NotBoundException | MalformedURLException | RemoteException | ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(SettlePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(AddCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(AddCustomerForm.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
//                paymentDetailPanel.setVisible(false);
                new Thread() {
                    @Override
                    public void run() {
                        paymentAmountTextField.setFocusable(false);
                        paymentAmountTextField.setText("Payment for this Order already reserved....");
                        paymentAmountTextField.setForeground(Color.red);
                        try {
                            Thread.sleep(3000);
                        } catch (InterruptedException ex) {
                            Logger.getLogger(SettlePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        paymentAmountTextField.setText("");
                        paymentAmountTextField.setForeground(Color.BLACK);
                    }
                }.start();
            }
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            Logger.getLogger(SettlePaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_orderTableMouseClicked

