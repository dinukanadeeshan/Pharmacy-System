    private void paymentAmountTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_paymentAmountTextFieldKeyReleased

        Validation.validatePriceText(paymentAmountTextField);
        settlePaymentButton.setEnabled(!paymentAmountTextField.getText().isEmpty() && !paymentRecivTextField.getText().isEmpty());
        String text = paymentAmountTextField.getText();
        if (text != null && !text.isEmpty()) {
            payment = Double.parseDouble(text);
            double due = Double.parseDouble(dueAmountTextField.getText());
            double balance = due - payment;
            int caretPosition = paymentAmountTextField.getCaretPosition();
            if (balance < 0) {
                text = text.substring(0, caretPosition - 1) + text.substring(caretPosition);
                paymentAmountTextField.setText(text);
                payment = Double.parseDouble(text);
            } else {
                balancePaymentTextField.setText(balance + "");
            }


        }
    }//GEN-LAST:event_paymentAmountTextFieldKeyReleased

